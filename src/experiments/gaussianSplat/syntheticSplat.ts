export const SYNTHETIC_SPLAT_COUNT = 16_000;
export const ANTISPLAT_BYTES_PER_SPLAT = 32;

type SplatRecord = {
  position: [number, number, number];
  scale: [number, number, number];
  color: [number, number, number, number];
};

function mulberry32(seed: number) {
  let value = seed >>> 0;
  return () => {
    value += 0x6d2b79f5;
    let result = value;
    result = Math.imul(result ^ (result >>> 15), result | 1);
    result ^= result + Math.imul(result ^ (result >>> 7), result | 61);
    return ((result ^ (result >>> 14)) >>> 0) / 4294967296;
  };
}

function hslToRgb(hue: number, saturation: number, lightness: number): [number, number, number] {
  const chroma = (1 - Math.abs(2 * lightness - 1)) * saturation;
  const section = ((hue % 1) + 1) % 1 * 6;
  const secondary = chroma * (1 - Math.abs(section % 2 - 1));
  const [r, g, b] = section < 1 ? [chroma, secondary, 0]
    : section < 2 ? [secondary, chroma, 0]
      : section < 3 ? [0, chroma, secondary]
        : section < 4 ? [0, secondary, chroma]
          : section < 5 ? [secondary, 0, chroma]
            : [chroma, 0, secondary];
  const match = lightness - chroma / 2;
  return [r + match, g + match, b + match].map((value) => Math.round(value * 255)) as [number, number, number];
}

function createTorusSplat(index: number, count: number, random: () => number): SplatRecord {
  const u = (index / count) * Math.PI * 2 * 7 + (random() - 0.5) * 0.08;
  const v = random() * Math.PI * 2;
  const majorRadius = 1.62;
  const tubeRadius = 0.42 + (random() - 0.5) * 0.08;
  const radius = majorRadius + tubeRadius * Math.cos(v);
  const rgb = hslToRgb(index / count * 0.68 + 0.48, 0.72, 0.58);
  return {
    position: [radius * Math.cos(u), tubeRadius * Math.sin(v) + 0.65, radius * Math.sin(u)],
    scale: [0.045, 0.032, 0.045],
    color: [...rgb, 210],
  };
}

function createOrbSplat(index: number, count: number, random: () => number): SplatRecord {
  const y = 1 - 2 * ((index + 0.5) / count);
  const radiusAtY = Math.sqrt(Math.max(0, 1 - y * y));
  const angle = index * Math.PI * (3 - Math.sqrt(5));
  const radius = 0.76 + (random() - 0.5) * 0.1;
  const rgb = hslToRgb(0.035 + random() * 0.045, 0.9, 0.62);
  return {
    position: [radiusAtY * Math.cos(angle) * radius, y * radius + 0.65, radiusAtY * Math.sin(angle) * radius],
    scale: [0.052, 0.052, 0.052],
    color: [...rgb, 225],
  };
}

function createGroundSplat(index: number, count: number, random: () => number): SplatRecord {
  const rowSize = Math.ceil(Math.sqrt(count));
  const row = Math.floor(index / rowSize);
  const column = index % rowSize;
  const x = (column / Math.max(1, rowSize - 1) - 0.5) * 7.5;
  const z = (row / Math.max(1, rowSize - 1) - 0.5) * 5.8;
  const line = row % 8 === 0 || column % 8 === 0;
  return {
    position: [x + (random() - 0.5) * 0.05, -0.82, z + (random() - 0.5) * 0.05],
    scale: line ? [0.055, 0.018, 0.055] : [0.04, 0.014, 0.04],
    color: line ? [80, 145, 214, 125] : [48, 58, 70, 90],
  };
}

function writeSplat(view: DataView, bytes: Uint8Array, index: number, splat: SplatRecord) {
  const offset = index * ANTISPLAT_BYTES_PER_SPLAT;
  splat.position.forEach((value, component) => view.setFloat32(offset + component * 4, value, true));
  splat.scale.forEach((value, component) => view.setFloat32(offset + 12 + component * 4, value, true));
  splat.color.forEach((value, component) => { bytes[offset + 24 + component] = value; });
  bytes[offset + 28] = 255;
  bytes[offset + 29] = 128;
  bytes[offset + 30] = 128;
  bytes[offset + 31] = 128;
}

export function createSyntheticSplatData(count = SYNTHETIC_SPLAT_COUNT) {
  const safeCount = Math.max(1, Math.floor(count));
  const buffer = new ArrayBuffer(safeCount * ANTISPLAT_BYTES_PER_SPLAT);
  const view = new DataView(buffer);
  const bytes = new Uint8Array(buffer);
  const random = mulberry32(0x3d2026);
  const torusCount = Math.floor(safeCount * 0.56);
  const orbCount = Math.floor(safeCount * 0.25);

  for (let index = 0; index < safeCount; index += 1) {
    const splat = index < torusCount
      ? createTorusSplat(index, torusCount, random)
      : index < torusCount + orbCount
        ? createOrbSplat(index - torusCount, orbCount, random)
        : createGroundSplat(index - torusCount - orbCount, safeCount - torusCount - orbCount, random);
    writeSplat(view, bytes, index, splat);
  }
  return bytes;
}

const SH_C0 = 0.28209479177387814;

function clampUnit(value: number) {
  return Math.min(1 - 1e-6, Math.max(1e-6, value));
}

export function createSyntheticPlyData(count = SYNTHETIC_SPLAT_COUNT) {
  const splat = createSyntheticSplatData(count);
  const safeCount = splat.byteLength / ANTISPLAT_BYTES_PER_SPLAT;
  const header = [
    "ply",
    "format binary_little_endian 1.0",
    "comment generated by 3D director Gaussian experiment",
    `element vertex ${safeCount}`,
    "property float x",
    "property float y",
    "property float z",
    "property float nx",
    "property float ny",
    "property float nz",
    "property float f_dc_0",
    "property float f_dc_1",
    "property float f_dc_2",
    "property float opacity",
    "property float scale_0",
    "property float scale_1",
    "property float scale_2",
    "property float rot_0",
    "property float rot_1",
    "property float rot_2",
    "property float rot_3",
    "end_header\n",
  ].join("\n");
  const headerBytes = new TextEncoder().encode(header);
  const floatsPerPoint = 17;
  const output = new Uint8Array(headerBytes.byteLength + safeCount * floatsPerPoint * 4);
  output.set(headerBytes);
  const inputView = new DataView(splat.buffer, splat.byteOffset, splat.byteLength);
  const outputView = new DataView(output.buffer);

  for (let index = 0; index < safeCount; index += 1) {
    const inputOffset = index * ANTISPLAT_BYTES_PER_SPLAT;
    const outputOffset = headerBytes.byteLength + index * floatsPerPoint * 4;
    const values = [
      inputView.getFloat32(inputOffset, true),
      inputView.getFloat32(inputOffset + 4, true),
      inputView.getFloat32(inputOffset + 8, true),
      0, 1, 0,
      ...[0, 1, 2].map((component) => ((splat[inputOffset + 24 + component] ?? 0) / 255 - 0.5) / SH_C0),
      Math.log(clampUnit((splat[inputOffset + 27] ?? 255) / 255) / (1 - clampUnit((splat[inputOffset + 27] ?? 255) / 255))),
      Math.log(Math.max(1e-6, inputView.getFloat32(inputOffset + 12, true))),
      Math.log(Math.max(1e-6, inputView.getFloat32(inputOffset + 16, true))),
      Math.log(Math.max(1e-6, inputView.getFloat32(inputOffset + 20, true))),
      (splat[inputOffset + 28] - 128) / 128,
      (splat[inputOffset + 29] - 128) / 128,
      (splat[inputOffset + 30] - 128) / 128,
      (splat[inputOffset + 31] - 128) / 128,
    ];
    values.forEach((value, component) => outputView.setFloat32(outputOffset + component * 4, value, true));
  }
  return output;
}

export function createSyntheticKsplatData(count = SYNTHETIC_SPLAT_COUNT) {
  const splat = createSyntheticSplatData(count);
  const safeCount = splat.byteLength / ANTISPLAT_BYTES_PER_SPLAT;
  const headerBytes = 4_096;
  const sectionHeaderBytes = 1_024;
  const bytesPerPoint = 44;
  const output = new Uint8Array(headerBytes + sectionHeaderBytes + safeCount * bytesPerPoint);
  const outputView = new DataView(output.buffer);
  output[0] = 0;
  output[1] = 1;
  outputView.setUint32(4, 1, true);
  outputView.setUint32(8, 1, true);
  outputView.setUint32(12, safeCount, true);
  outputView.setUint32(16, safeCount, true);
  outputView.setUint16(20, 0, true);
  outputView.setFloat32(36, -1.5, true);
  outputView.setFloat32(40, 1.5, true);

  const sectionOffset = headerBytes;
  outputView.setUint32(sectionOffset, safeCount, true);
  outputView.setUint32(sectionOffset + 4, safeCount, true);
  outputView.setUint32(sectionOffset + 28, safeCount * bytesPerPoint, true);
  outputView.setUint16(sectionOffset + 40, 0, true);

  const inputView = new DataView(splat.buffer, splat.byteOffset, splat.byteLength);
  const dataOffset = headerBytes + sectionHeaderBytes;
  for (let index = 0; index < safeCount; index += 1) {
    const inputOffset = index * ANTISPLAT_BYTES_PER_SPLAT;
    const outputOffset = dataOffset + index * bytesPerPoint;
    for (let component = 0; component < 6; component += 1) {
      outputView.setFloat32(outputOffset + component * 4, inputView.getFloat32(inputOffset + component * 4, true), true);
    }
    outputView.setFloat32(outputOffset + 24, (splat[inputOffset + 28] - 128) / 128, true);
    outputView.setFloat32(outputOffset + 28, (splat[inputOffset + 29] - 128) / 128, true);
    outputView.setFloat32(outputOffset + 32, (splat[inputOffset + 30] - 128) / 128, true);
    outputView.setFloat32(outputOffset + 36, (splat[inputOffset + 31] - 128) / 128, true);
    output.set(splat.subarray(inputOffset + 24, inputOffset + 28), outputOffset + 40);
  }
  return output;
}
